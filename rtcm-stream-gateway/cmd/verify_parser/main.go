package main

import (
	"fmt"

	"github.com/your-org/rtcm-stream-gateway/internal/rtcm"
)

func main() {
	frames := [][]byte{
		{0xd3, 0x00, 0x13, 0x3e, 0xd0, 0x01, 0x52, 0x7f, 0x5f, 0x97, 0xd6, 0x38, 0x3f, 0x00, 0x29, 0xbc, 0xc0, 0x00, 0xe5, 0xc8, 0xa5, 0x60, 0x29, 0xd6, 0xa3},
		{0xd3, 0x00, 0x15, 0x3e, 0xe0, 0x01, 0x52, 0x7f, 0x5f, 0x97, 0xd6, 0x38, 0x3f, 0x00, 0x29, 0xbc, 0xc0, 0x00, 0xe5, 0xc8, 0xa5, 0x60, 0x00, 0x96, 0xd9, 0x8c, 0x0f},
	}

	allOk := true
	for i, f := range frames {
		msg, msgOk := rtcm.MessageType(f)
		sid, sidOk := rtcm.StationID(f)
		fp, fpOk := rtcm.StationFingerprint(f)
		fmt.Printf("Frame %d: msg=%d(%v) station=%d(%v) fingerprint=%s(%v)\n", i, msg, msgOk, sid, sidOk, fp, fpOk)
		if !msgOk || !sidOk {
			allOk = false
		}
	}

	if allOk {
		fmt.Println("All frames parsed correctly!")
	}
}
